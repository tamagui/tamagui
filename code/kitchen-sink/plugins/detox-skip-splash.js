// skip Expo SplashScreen registration when launched by Detox.
//
// the SplashScreenManager.registerOnActivity call installs an OnPreDrawListener that
// returns false on every frame until JS calls SplashScreen.hideAsync(). That
// continuous re-draw keeps the main looper from ever going idle, which trips
// AndroidX MonitoringInstrumentation.startActivitySync's hardcoded 45s idle
// timeout on slow CI emulators where bundle download + eval takes longer than
// that. Without this fix, every Detox-driven launch on the GitHub Actions
// software-graphics emulator fails with:
//   "Could not launch intent ... within 45000 milliseconds. Perhaps the main
//    thread has not gone idle within a reasonable amount of time?"
//
// the android/ folder is gitignored (regenerated by expo prebuild), so this
// plugin re-injects the patch on every prebuild.
const MARKER = 'isUnderDetox'

function patchMainActivity(source) {
  if (source.includes(MARKER)) return source

  const registration = 'SplashScreenManager.registerOnActivity(this)'
  if (!source.includes(registration)) {
    throw new Error(
      'detox-skip-splash plugin: could not find SplashScreenManager registration'
    )
  }

  // the splash manager normally replaces Theme.App.SplashScreen with the
  // AppCompat-based AppTheme before ReactActivity.onCreate; preserve that
  // theme switch when Detox bypasses the splash listener.
  const patched = source.replace(
    registration,
    'if (isUnderDetox()) {\n      setTheme(R.style.AppTheme)\n    } else {\n      SplashScreenManager.registerOnActivity(this)\n    }'
  )

  // insert the helper before the final closing brace of the Kotlin class.
  const helper = `
  private fun isUnderDetox(): Boolean {
    // the Detox.LaunchArgs.asIntentBundle() result copies all instrumentation args
    // (including detoxServer / detoxSessionId) into the activity's
    // "launchArgs" bundle extra.
    val launchArgs = intent?.getBundleExtra("launchArgs") ?: return false
    return launchArgs.containsKey("detoxServer") ||
      launchArgs.containsKey("detoxSessionId")
  }
`

  const lastBrace = patched.lastIndexOf('}')
  if (lastBrace === -1) {
    throw new Error(
      'detox-skip-splash plugin: could not find closing brace in MainActivity.kt'
    )
  }
  return patched.slice(0, lastBrace) + helper + patched.slice(lastBrace)
}

function withDetoxSkipSplash(config) {
  const { withMainActivity } = require('@expo/config-plugins')
  return withMainActivity(config, (config) => {
    config.modResults.contents = patchMainActivity(config.modResults.contents)
    return config
  })
}

module.exports = withDetoxSkipSplash
module.exports.patchMainActivity = patchMainActivity
